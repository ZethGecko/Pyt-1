export class IconComponent {
  @Input() name: string = '';
  @Input() size: IconSize = 'md';
  @Input() customClass: string = '';
  @Input() ariaLabel: string = '';

  private sizeClasses: Record<IconSize, string> = {
    'xs': 'w-3 h-3',
    'sm': 'w-4 h-4',
    'md': 'w-5 h-5',
    'lg': 'w-6 h-6',
    'xl': 'w-8 h-8',
    '2xl': 'w-10 h-10'
  };

  get iconPath(): string | null {
    return ICON_PATHS[this.name] || null;
  }

  get iconPaths(): string[] | null {
    return MULTI_PATH_ICONS[this.name] || null;
  }

  get svgClasses(): string {
    const classes = [this.sizeClasses[this.size]];
    if (this.customClass) {
      classes.push(this.customClass);
    }
    return classes.join(' ');
  }

  /**
   * Check if an icon name exists in the registry
   */
  static hasIcon(name: string): boolean {
    return !!(ICON_PATHS[name] || MULTI_PATH_ICONS[name]);
  }

  /**
   * Get all available icon names
   */
  static getAvailableIcons(): string[] {
    return [...Object.keys(ICON_PATHS), ...Object.keys(MULTI_PATH_ICONS)];
  }

  /**
   * Check if current icon name is valid
   */
  get isValidIcon(): boolean {
    return !!(this.iconPath || this.iconPaths);
  }
}
